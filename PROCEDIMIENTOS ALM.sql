USE `workable`;
DROP procedure IF EXISTS `P_EMPRESA`;

DELIMITER $$
USE `workable`$$
CREATE PROCEDURE `P_EMPRESA` (
IN P_NOMB_EMPRESA VARCHAR(100),
IN P_DESCRIPCION_EMPR TEXT,
IN P_DIRECCION_EMPR VARCHAR(100),
IN P_CATEG_ID INT,
IN P_CIUD_ID INT
)
BEGIN
	INSERT INTO EMPRESA (NOMB_EMPRESA, DESCRIPCION_EMPR, DIRECCION_EMPR, CATEG_ID, CIUD_ID) 
    VALUES (P_NOMB_EMPRESA, P_DESCRIPCION_EMPR, P_DIRECCION_EMPR, P_CATEG_ID, P_CIUD_ID);
END$$

DELIMITER ;