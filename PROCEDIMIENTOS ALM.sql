# INSERTAR EMPRESA

USE `workable`;
DROP procedure IF EXISTS `P_EMPRESA`;

DELIMITER $$
USE `workable`$$
CREATE PROCEDURE `P_EMPRESA` (
IN P_NOMB_EMPRESA VARCHAR(100),
IN P_DESCRIPCION_EMPR TEXT,
IN P_DIRECCION_EMPR VARCHAR(100),
IN P_CATEG_ID INT,
IN P_MUNI_ID INT
)
BEGIN
	INSERT INTO EMPRESA (NOMB_EMPRESA, DESCRIPCION_EMPR, DIRECCION_EMPR, CATEG_ID, MUNI_ID) 
    VALUES (P_NOMB_EMPRESA, P_DESCRIPCION_EMPR, P_DIRECCION_EMPR, P_CATEG_ID, P_MUNI_ID);
END$$

DELIMITER ;



# INSERTAR USUARIO

USE `workable`;
DROP procedure IF EXISTS `P_USUARIO`;

DELIMITER $$
USE `workable`$$
CREATE PROCEDURE `P_USUARIO` (
IN P_NOMBRE VARCHAR(50),
IN P_APELLIDO VARCHAR(50),
IN P_CLAVE VARCHAR(255),
IN P_CORREO VARCHAR(255),
IN P_USR_NAME VARCHAR(255),
IN P_MUNI_ID INT,
IN P_EMPR_ID INT,
IN P_ROL_ID TINYINT,
IN P_ID_TIP_DOC INT
)

BEGIN
	INSERT INTO USUARIO (NOMBRE, APELLIDO, CLAVE, CORREO, USR_NAME, MUNI_ID, EMPR_ID, ROL_ID, ID_TIP_DOC) 
    VALUES (P_NOMBRE, P_APELLIDO, P_CLAVE, P_CORREO, P_USR_NAME, P_MUNI_ID, P_EMPR_ID, P_ROL_ID, P_ID_TIP_DOC);
END$$

DELIMITER ;